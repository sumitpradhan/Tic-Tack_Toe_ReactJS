//This function helps in determining wether the player has won or not.
export default function calculateWinner(squares) {
  const lines = [
    [0, 1, 2],
    [3, 4, 5],
    [6, 7, 8],
    [0, 3, 6],
    [1, 4, 7],
    [2, 5, 8],
    [0, 4, 8],
    [2, 4, 6],
  ];
  //The if condition will see if Any of the pattern(we know tha a player wins only if diagonals or horizontals are same ) , this for loop will iterate
  // over all the winning patterns and check if any one of them has either all "X" or all "O".
  for (let i = 0; i < lines.length; i++) {
    const [a, b, c] = lines[i];
    if (squares[a] && squares[a] === squares[b] && squares[a] === squares[c]) {
      return squares[a];
    }
  }
  return null;
}
